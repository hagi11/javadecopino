    public void ajustarPermisos() {

        if (usuarioPermisos.get(4).getEditar() == 0) {
            btnEditar.setVisible(false);
        }
    }

    public void inicio() {
        CtrAuxiliares ctra = new CtrAuxiliares();
        txtRol.setText(usuario.getRol().getNombre());
        txtTipoIdentificacion.setText(ctra.mostrarTipoIdentId(usuario.getTidenrificacion()));
        txtIdentificacion.setText(String.valueOf(usuario.getIdentificacion()));
        txtNombre.setText(usuario.getNombre());
        txtApellido.setText(usuario.getApellido());
        txtCorreo.setText(usuario.getLogin());
        txtDireccion.setText(usuario.getDireccion());
        txtTelefono.setText(usuario.getTelefono());
        txtCiudad.setText(usuario.getCiudad().getCiudad());
        txtFechaCreacion.setText(usuario.getFregistro().toString().substring(0, 11));
        unebla();
    }

    public void unebla() {
        txtRol.setEditable(false);
        txtTipoIdentificacion.setEditable(false);
        txtIdentificacion.setEditable(false);
        txtNombre.setEditable(false);
        txtApellido.setEditable(false);
        txtCorreo.setEditable(false);
        txtDireccion.setEditable(false);
        txtTelefono.setEditable(false);
        txtCiudad.setEditable(false);
        txtFechaCreacion.setEditable(false);

    }

    public void irEditar() {
        vstAgregarEditarUsuario panel = new vstAgregarEditarUsuario(usuario.getId());
        vstMenu.panelContenedor(panel);
        
    }

    public void cancer() {
        vstVerUsuario panel = new vstVerUsuario();
        vstMenu.panelContenedor(panel);
    }

